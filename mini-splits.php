                                        <h1>What is a ductless mini-split system?</h1>

                                        <p>Ductless mini split heating and cooling systems are highly efficient heating and cooling systems that deliver warm or cool conditioned air directly into different rooms in your Indiana home, without the use of ducting. You may also hear them called mini-split and multi-split heat pumps or ductless mini-split air conditioning systems.
                                        </p>

                                        <br>

                                        <p>Ductless mini-split heating and cooling units use small heating and cooling
                                            air handlers for every individual room, so installing a ductless mini-split
                                            system makes it easy to set up heating and cooling zones in your home. This
                                            means each room in your home can be at a different temperature and comfort
                                            level.
                                            <br><br>
                                            Some of our mini split ductless systems can accommodate up to eight indoor
                                            units that can provide heating or cooling, and each of these air handlers
                                            can be controlled individually via its own wireless thermostat.
                                            <br><br>
                                            Since mini splits have no need for ducts, they avoid the energy losses
                                            associated with the ductwork of central air systems. Heated or cooled air
                                            lost through leaky or uninsulated ducts in your home, can account for more
                                            than 30% of energy consumption for space conditioning, especially if the
                                            ducts are in an unconditioned space such as an attic. This makes a ductless
                                            mini-split highly energy efficient.

                                        </p>





